        # æ·»åŠ æœˆä»½åç§°å‚æ•°
        format_params = {
            "year": self.year,
            "month": self.month,
            "day": self.day,
            "month_abbr": self.__month_names[self.month - 1],
            "month_name": self.__month_full_names[self.month - 1]
        }

        return format_template.format(**format_params)

